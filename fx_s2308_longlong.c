fx_s2308 fx_s2308_sind(fx_s2308 fa)
{
    int sign = 1;
    fixed32 ret0, diff;
    int idx;
    if (fa < 0)
    {
        sign = -1;
        fa *= -1;
    }
    fa = fa % FX32_360;
    if (fa >= FX32_180)
    {
        sign *= -1;
        fa -= FX32_180;
    }
    if (fa > FX32_90)
        fa = FX32_180 - fa;
    idx = fa >> 8;
    ret0 = fx_s2308_SinTable[idx];
    diff = fx_s2308_SinTable[idx + 1] - ret0;
    return (sign * (ret0 + ((diff * (fa & 0x00FF)) >> 8)));
}
